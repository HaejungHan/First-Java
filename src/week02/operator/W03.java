package week02.operator;

public class W03 {
    // 비교연산자 예제
    public static void main(String[] args) {
        System.out.println(10 > 9); // 10이 9보다 크다 true
        System.out.println(10 >= 9); // 10이 9보다 크거나 같다 true
        System.out.println(10 < 9); // 10이 9보다 작다 false
        System.out.println(10 <= 9); // 10이 9보다 작거나 같다 false
        System.out.println(10 == 10); // 10이 10이랑 같다 true
        System.out.println(10 == 9); // 10이 9랑 같다 false
        System.out.println(10 != 10); // 10은 10이랑 같지 않다. false
        System.out.println(10 != 9); // 10은 9랑 같지 않다. true

    }
}
